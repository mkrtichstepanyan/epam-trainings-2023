package homework_3.Anna_Manukyan.chapter_5;

//Demonstrate for loop
public class Sample {
    public static void main(String[] args) {
        int a;
        int b = 4;
        for (a = 1; a < b; a++) {
            System.out.println("a = " + a);
            System.out.println("b = " + b);
            b--;
        }
    }
}
